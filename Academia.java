public class Academia {
    //Atributos
    private String nombre;
    //Constrcutores 
    public Academia(){
    }
    public Academia(String nombre){
        this.nombre=nombre;
    }
    //Getters
    public String getNombre() {
        return nombre;
    }
    //Setters
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
