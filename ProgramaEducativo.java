public class ProgramaEducativo {
    //Atributos
    private String nombre;
    //Contructores
    public ProgramaEducativo(){
    }
    public ProgramaEducativo(String nombre){
        this.nombre=nombre;
    }
    //Getters
    public String getNombre() {
        return nombre;
    }
    //Setters
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
